# Solicita o primeiro nome do usuário
primeiro_nome = input("Digite o seu primeiro nome: ")

# Solicita o último nome do usuário
ultimo_nome = input("Digite o seu último nome: ")

# Concatena o primeiro e último nome para formar o nome completo
nome_completo = primeiro_nome + " " + ultimo_nome

# Exibe o nome completo na tela
print("O seu nome completo é:", nome_completo)